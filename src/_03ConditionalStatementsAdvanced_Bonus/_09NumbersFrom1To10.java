package _03ConditionalStatementsAdvanced_Bonus;

public class _09NumbersFrom1To10 {
    public static void Numbers1To10() {
        int num = 1;

        while(num <= 10) {
            System.out.println(num);
            num++;
        }
    }

    public static void main(String[] args) {
        Numbers1To10();
    }
}